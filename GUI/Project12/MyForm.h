	private:System::Void displayTotal() {
		double CPUprice = selectedCPU.getPrice();
		double GPUprice = selectedGPU.getPrice();
		double PSUprice = selectedPSU.getPrice();
		double Caseprice = selectedCase.getPrice();
		double Storageprice = selectedStorage.getPrice();
		double Motherboardprice = selectedMobo.getPrice();
		double Ramprice = selectedRAM.getPrice();
		double Coolerprice = selectedCooler.getPrice();
		double Subtotal = CPUprice + GPUprice + PSUprice + Storageprice + Motherboardprice + Ramprice + Caseprice + Coolerprice;

		double tax = Subtotal * 0.13;
		double Total = Subtotal + tax;
		textBox1->Text = Subtotal.ToString();
		label41->Text = Subtotal.ToString();
		label42->Text = Total.ToString();
	}


	private:System::Boolean Compatibility() {   // FUNCTION TO CHECK IF SELECTED COMPONENTS ARE COMPATIBLE 
		if (selectedCPU.getSocket() != selectedMobo.getSocket())
		{
			String^ socket = "This CPU is Incompatible with your selected Motherboard";
			MessageBox::Show(socket, "Incompatibility Error", MessageBoxButtons::OK, MessageBoxIcon::Error);
			return false;
		}
		//Add tdp for CPU and GPU, add 150 for other components. Multiply by 30% to account for power spikes			
		int Wattage = (selectedCPU.getTDP() + selectedGPU.getTDP() + 150);
		Wattage *= 0.3;

		if (Wattage >= selectedPSU.getWattage())
		{
			String^ power = "Your Power Supply isn't strong enough for your selected components";
			MessageBox::Show(power, "Incompatibility Error", MessageBoxButtons::OK, MessageBoxIcon::Error);
			return false;
		}
		if ((selectedMobo.getChipset() == "AM5" || selectedMobo.getChipset() == "Z790") && (!selectedRAM.getDDR5()))
		{
			String^ Ramissue = "This Ram is Incompatible with your selected Motherboard";
			MessageBox::Show(Ramissue, "Incompatibility Error", MessageBoxButtons::OK, MessageBoxIcon::Error);
			return false;
		}
		if ((selectedMobo.getChipset() != "AM5" || selectedMobo.getChipset() != "Z790") && (!selectedRAM.getDDR5()))
		{
			String^ Ramissue = "This Ram is Incompatible with your selected Motherboard";
			MessageBox::Show(Ramissue, "Incompatibility Error", MessageBoxButtons::OK, MessageBoxIcon::Error);
			return false;
		}
		if (selectedMobo.getForm() == "ATX" && (selectedCase.getSize() == "Mini ITX " || selectedCase.getSize() == "Micro ATX"))
		{
			String^ casesize = "This Case isn't big enough for your selected Motherboard";
			MessageBox::Show(casesize, "Incompatibility Error", MessageBoxButtons::OK, MessageBoxIcon::Error);
			return false;
		}
		else if (selectedMobo.getForm() == "Micro ATX" && selectedCase.getSize() == "Mini ITX")
		{
			String^ casesize = "This Case isn't big enough for your selected Motherboard";
			MessageBox::Show(casesize, "Incompatibility Error", MessageBoxButtons::OK, MessageBoxIcon::Error);
			return false;
		}

		return true; // RETURN TRUE IF ALL COMPATIBLE 

	}

